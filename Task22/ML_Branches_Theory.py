''' Supervised Learning: In supervised learning, the algorithm is trained on labeled data, which means the data 
is already categorized and labeled. The algorithm learns from this labeled data to make predictions or
decisions when it encounters new, unseen data. This type of learning is commonly used for tasks such as 
classification or regression. '''

''' Unsupervised Learning: In unsupervised learning, the algorithm is trained on unlabeled data, which means
  the data is not categorized or labeled. The algorithm learns from this data to find patterns, 
 relationships, or groupings within the data. This type of learning is commonly used for tasks such as 
 clustering or anomaly detection.'''

''' Semi-Supervised Learning: Semi-supervised learning is a combination of supervised and unsupervised learning.
  In this type of learning, the algorithm is trained on a small amount of labeled data and a large amount of
  unlabeled data. The algorithm learns from both types of data to make predictions or decisions when it 
 encounters new, unseen data. This type of learning is commonly used when labeled data is difficult or
  expensive to obtain.'''

''' Reinforcement Learning: In reinforcement learning, the algorithm learns through trial and error. The
  algorithm interacts with an environment and learns to take actions that maximize a reward signal. 
 The algorithm learns from feedback in the form of rewards or penalties for its actions. This type of
  learning is commonly used in robotics or gaming applications. '''