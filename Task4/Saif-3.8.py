places = ['Australia', 'NewZealand', 'Saudia Arab', 'Egypt']

print("Original order:")
print(places)


print("Alphabetic order:")
print(sorted(places))


print("Original order (again):")
print(places)


print("Reverse alphabetical order:")
print(sorted(places, reverse=True))


print("Original order (again):")
print(places)


places.reverse()


print("Reversed order:")
print(places)


places.reverse()


print("Original order (again):")
print(places)


places.sort()


print("Alphabetical order:")
print(places)


places.sort(reverse=True)


print("Reverse alphabetical order:")
print(places)
