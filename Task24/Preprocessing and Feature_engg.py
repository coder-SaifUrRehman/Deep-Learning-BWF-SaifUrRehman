'''
Data preprocessing involves cleaning and transforming raw data into a format that is suitable for machine 
learning algorithms. This can include tasks such as removing missing values, handling outliers, scaling 
features, and encoding categorical variables. The goal of data preprocessing is to improve the quality of the
 data and make it more suitable for modeling.

Data Preprocessing includes: 
Handling missing data: filling in missing values with mean or median, or dropping missing values
Handling outliers: removing or capping extreme values
Scaling features: normalizing or standardizing data to have zero mean and unit variance
Encoding categorical variables: converting categorical data to numerical format using techniques such as one-hot encoding or label encoding
Handling imbalanced classes: balancing the number of samples in each class by oversampling or undersampling
'''


'''
Feature engineering involves creating new features from the existing data that may be more informative or 
useful for the model. This can include tasks such as extracting text features, creating interaction terms, 
and transforming variables. Feature engineering can help improve the performance of the model by providing
 more relevant and informative input.

Feature Enggineering includes: 
Creating new features: deriving new features from existing ones, such as calculating ratios or differences between variables
Text feature extraction: converting text data into numerical format using techniques such as bag-of-words or TF-IDF
Dimensionality reduction: reducing the number of features using techniques such as principal component analysis (PCA) or t-SNE
Time series feature engineering: creating features that capture patterns and trends in time series data, such as rolling averages or lagged variables
Interaction terms: creating new features by multiplying or adding existing features together to capture interactions between them.  
'''